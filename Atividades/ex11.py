larg = float(input('Insira a largura da parede: '))
altu = float(input('Insire a altura da parede: '))
area = larg * altu
tinta =  area / 2
print(f'Sua parede tem a dimensão de {larg}x{altu} correspondendo a uma área de {area:.2f}m²')
print(f'Você precisara de {tinta:.2f}L para pintar essa parede!')


